int	ft_is_prime(int nb)
{
	int	i;
	int	j;

	i = 2;
	j = 1;
	if (nb > 2147483647)
		return (2147483647);
	if (nb < 0 || nb == 0 || nb == 1)
		return (0);
	while (i <= nb / 2)
	{
		if (nb % i == 0)
		{
			j = 0;
			break ;
		}
		++i;
	}
	if (j == 0)
		return (0);
	else
		return (1);
}
