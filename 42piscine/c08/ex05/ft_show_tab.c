#include <unistd.h>
#include "ft_stock_str.h"

void	ft_putstr(char *s);
void	ft_putnbr(int nb);
void	ft_putchar(char s);

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str != 0)
	{
		ft_putstr(par[i].str);
		write (1, "\n", 1);
		ft_putnbr(par[i].size);
		write (1, "\n", 1);
		ft_putstr(par[i].copy);
		write (1, "\n", 1);
		i++;
	}
}

void	ft_putstr(char *s)
{
	int	i;

	i = 0;
	while (s[i] != 0)
	{
		write (1, &s[i], 1);
		i++;
	}
}

void	ft_putnbr(int nb)
{
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		nb = nb % 10;
	}
	if (nb < 10)
	{
		ft_putchar(nb + '0');
	}
}

void	ft_putchar(char s)
{
	write (1, &s, 1);
}
